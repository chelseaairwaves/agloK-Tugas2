# Program konversi satuan dari meter ke centimeter

# Masukkan angka yang akann diubah (angka dalam meter)
meter = float(input("Masukkan angka dalam meter: "))

# Melakukan konversi ke centimeter
centimeter = meter * 100  # 1 meter sama dengan 100 cm

# Menampilkan hasil konversi ke layar
print(f"Hasil dalam centimeter: {int(centimeter)} cm")

